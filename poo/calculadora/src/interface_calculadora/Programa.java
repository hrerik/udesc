    ActionListener digitarNumeros = new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            JButton botao = (JButton) e.getSource();
            String digito = botao.getText();

            if (digitandoPrimeiroNumero) {
                if (num1Txt.length() < 3) {
                    num1Txt += digito;
                    num1 = Integer.parseInt(num1Txt);
                    Display.setText(num1Txt);
                }
            } else if (num2Txt.length() < 3) {
                num2Txt += digito;
                num2 = Integer.parseInt(num2Txt);
                Display.setText(num1Txt + " " + op + " " + num2Txt);
                mostrarResultado();
            }

        }
    };

    void mostrarResultado() {
        try {
            // Verificar se há valores válidos
            if (!num2Txt.isEmpty() && !num1Txt.isEmpty()) {
                if (op.equals("/") && num2 == 0) {
                    DisplayResult.setText("= indefinido");
                    return;
                }

                // Usar operações seguras para detectar overflow
                int resultado = 0;
                switch (op) {
                    case "+" -> {
                        resultado = Math.addExact(num1, num2);
                    }
                    case "-" -> {
                        resultado = Math.subtractExact(num1, num2);
                    }
                    case "*" -> {
                        resultado = Math.multiplyExact(num1, num2);
                    }
                    case "/" -> {
                        if (num2 != 0) {
                            resultado = num1 / num2;
                        }
                    }
                }

                result = resultado;
                DisplayResult.setText("= " + Integer.toString(result));
            }
        } catch (ArithmeticException ex) {
            // Tratar overflow
            DisplayResult.setText("= Overflow");
            System.out.println("Overflow detectado: " + ex.getMessage());
        }
    }

    ActionListener digitarOp = new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            JButton botao = (JButton) e.getSource();
            op = botao.getText();
            digitandoPrimeiroNumero = false;
            if (num2Txt.equals("")) {
                Display.setText(num1Txt + " " + op);
            } else {
                Display.setText(num1Txt + " " + op + " " + num2Txt);
                mostrarResultado();
            }
        }
    };

