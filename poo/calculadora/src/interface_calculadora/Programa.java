    private void ButtIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtIgualActionPerformed
        if (!(DisplayResult.getText().equals("") || DisplayResult.getText().equals("= indefinido"))) {
            System.out.println(result);
            num1 = result;
            num2 = 0;
            num1Txt = Integer.toString(result);
            num2Txt = "";
            op = "";
            DisplayResult.setText("");
            Display.setText(num1Txt);
            digitandoPrimeiroNumero = true;
        }
    }//GEN-LAST:event_ButtIgualActionPerformed

    private void BackspaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BackspaceActionPerformed
        if (digitandoPrimeiroNumero) {
            if (num1Txt.length() > 0) {
                num1Txt = num1Txt.substring(0, num1Txt.length() - 1);
                try {
                    num1 = Integer.parseInt(num1Txt);
                }
                catch (NumberFormatException ex) {
                    num1 = 0;
                    num1Txt = "";
                }
                Display.setText(num1Txt);
            }
        } else {
            if (num2Txt.length() > 0) {
                num2Txt = num2Txt.substring(0, num2Txt.length() - 1);
                if (num2Txt.length() == 0) {
                    result = 0;
                    DisplayResult.setText("");
                    Display.setText(num1Txt + " " + op + " ");
                } else {
                    Display.setText(num1Txt + " " + op + " " + num2Txt);
                    num2 = Integer.parseInt(num2Txt);
                    mostrarResultado();
                }
            } else {
                op = "";
                digitandoPrimeiroNumero = true;
                Display.setText(num1Txt);
            }
        }
    }//GEN-LAST:event_BackspaceActionPerformed

